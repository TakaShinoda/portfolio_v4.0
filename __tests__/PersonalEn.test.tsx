import React from 'react'
import { render, screen } from '@testing-library/react'
import { Personal } from '../components/Personal'

jest.mock('next/router', () => ({
  useRouter() {
    return {
      locale: 'en',
    }
  },
}))

describe('英語での表示項目テスト', () => {
  test('氏名が表示されているか', () => {
    render(<Personal />)
    expect(screen.getByRole('heading')).toHaveTextContent('Takahiro SHINODA')
  })

  test('所在地が表示されているか', () => {
    render(<Personal />)
    expect(screen.getByText('Tokyo'))
  })

  test('会社名が表示されているか', () => {
    render(<Personal />)
    expect(screen.getByText('Bengo4.com,Inc.'))
  })

  test('一言が表示されているか', () => {
    render(<Personal />)
    expect(screen.getByText('Frontend Developer at CloudSign'))
  })
})
